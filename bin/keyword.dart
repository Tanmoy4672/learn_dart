//daynamic keyword
//final keyword
//const keyword

void main() {
  //we can use dynamic keyword replaced by var keyword
  dynamic number = 45;
  print(number);

  final Number1 = 50;
  // Number1 = 67; it will not work 'cos its a final datatype. it's a fixed data type
  print(Number1);

  const Number2 = 50;
  // Number2 = 67; it will not work 'cos its a const datatype , it's a fixed datatype
  print(Number2);
}
